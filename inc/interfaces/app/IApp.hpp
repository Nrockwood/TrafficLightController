#ifndef INCLUDE_IAPP_H_
#define INCLUDE_IAPP_H_

////////////////////////////////////////////////////////////
///  @brief Interface App Class
///  
////////////////////////////////////////////////////////////
class IApp
{
public:
    ////////////////////////////////////////////////////////////
    ///  @brief Destroy the IApp object
    ///  
    ////////////////////////////////////////////////////////////
    virtual ~IApp() = default;

    ////////////////////////////////////////////////////////////
    ///  @brief Initialize all necessary members for this app.
    ///  
    ////////////////////////////////////////////////////////////
    virtual void initApp() = 0;

    ////////////////////////////////////////////////////////////
    ///  @brief Execute the logic of this app when called upon.
    ///  
    ////////////////////////////////////////////////////////////
    virtual void run() = 0;
};

#endif // INCLUDE_IAPP_H_
